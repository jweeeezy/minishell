#include "libft.h"

int	ft_isalnum(int C)
{
	if (C >= '0' && C <= '9')
		return (1);
	if ((C >= 'a' && C <= 'z') || (C >= 'A' && C <= 'Z'))
		return (1);
	return (0);
}
